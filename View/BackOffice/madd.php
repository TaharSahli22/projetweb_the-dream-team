if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $name = $_POST['name'];
    $description = $_POST['description'];
    $price = $_POST['price'];
    $image = $_POST['image']; 

    if (!empty($name) && !empty($description) && !empty($price) && !empty($image)) {
        $db = new database();
        $conn = $db->getConnexion();

        $query = "INSERT INTO monuments (name, description, price, image) VALUES (:name, :description, :price, :image)";
        $stmt = $conn->prepare($query);

        $stmt->bindParam(':name', $name);
        $stmt->bindParam(':description', $description);
        $stmt->bindParam(':price', $price);
        $stmt->bindParam(':image', $image);

        if ($stmt->execute()) {
            echo "Monument Added Successfully.";
        } else {
            echo "Error.";
        }
    } else {
        echo "All Blanks Must Be Filled.";
    }
