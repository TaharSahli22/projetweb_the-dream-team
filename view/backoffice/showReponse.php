<script>
    const updatePopup = document.getElementById('updatePopup');
    const popupReponseId = document.getElementById('popupReponseId');
    const popupReponseMessage = document.getElementById('popupReponseMessage');
    const closePopupButtons = document.querySelectorAll('.closePopupBtn');

    // Function to open the popup with data
    function openUpdatePopup(id, message) {
        popupReponseId.value = id;
        popupReponseMessage.value = message;
        updatePopup.style.display = 'flex';
    }

    // Close popup when clicking "Cancel" or outside the content
    closePopupButtons.forEach(button => {
        button.addEventListener('click', () => {
            updatePopup.style.display = 'none';
        });
    });

    updatePopup.addEventListener('click', (e) => {
        if (e.target === updatePopup) {
            updatePopup.style.display = 'none';
        }
    });
</script>



